# Definir una lista con diferentes tipos de elementos
lista = ["Hola", 15, True, "Adios"]
# Agregar "Python" al final de la lista
lista.append("Python")
# Insertar el valor 17 en la posición 2 de la lista
lista.insert(2, 17)
# Eliminar el elemento en la posición 2 de la lista
lista.pop(2)
# Definir otra lista de números
lista2 = [5, 12, 7, 9, 15]
# Ordenar la lista2 en orden ascendente
lista2.sort()
