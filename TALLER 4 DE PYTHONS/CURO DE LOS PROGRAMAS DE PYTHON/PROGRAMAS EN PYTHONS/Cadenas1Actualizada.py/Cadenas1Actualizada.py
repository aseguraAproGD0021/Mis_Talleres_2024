# Definimos una cadena
cadena = 'HOLA'
# Usamos el método isalpha()
#para verificar si todos los caracteres son alfabéticos
print(cadena.isalpha())
# Salida esperada: True
# Usamos el método lower()
#para convertir la cadena a minúsculas
cadena2 = cadena.lower()
print(cadena2)
# Salida esperada: hola
# Usamos el método capitalize()
#para capitalizar la primera letra de la cadena
print(cadena2.capitalize())
# Salida esperada: Hola
# Definimos una cadena larga utilizando triple comillas
